package BuclesPimeraEva;

public class ejebu12 {
    public static void main(String[] args) {
        System.out.println("La primeras 10 tablas de multiplicar son:");
        for (int i = 1; i <= 10; i++) {
            for (int j = 0; j <= 10; j++) {
                System.out.println(i + " X " + j + " = " + i*j);
            }
            System.out.println("-----------------------------");
        }
    }
}
